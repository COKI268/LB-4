colors = {
    "красный": (255, 0, 0),
    "зеленый": (0, 255, 0), 
    "синий": (0, 0, 255),
    "желтый": (255, 255, 0),
    "фиолетовый": (128, 0, 128) }

mix = tuple((a + b) // 2 for a, b in zip(colors["красный"], colors["синий"]))
print(f"Красный + синий: {mix}")

invert = tuple(255 - c for c in colors["желтый"])
print(f"Инверт желтого: {invert}")
